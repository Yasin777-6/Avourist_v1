def calculate_payment_split(total_amount: int, option: str = 'option_2') -> dict:
    """
    Calculate payment split based on option
    
    Args:
        total_amount: Total contract amount
        option: Payment option key
        
    Returns:
        dict with prepayment and remainder amounts
    """
    payment_option = PAYMENT_OPTIONS.get(option, PAYMENT_OPTIONS['option_2'])
    prepayment_percent = payment_option['prepayment_percent']
    
    prepayment = int(total_amount * prepayment_percent / 100)
    remainder = total_amount - prepayment
    
    return {
        'option_name': payment_option['name'],
        'prepayment': prepayment,
        'remainder': remainder,
        'description': payment_option['description']
    }
