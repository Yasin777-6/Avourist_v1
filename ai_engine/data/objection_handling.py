def get_objection_response(message: str) -> dict:
    """
    Detect objection in message and return appropriate response
    
    Args:
        message: Client message
        
    Returns:
        dict with 'detected' (bool) and 'response' (str)
    """
    message_lower = message.lower()
    
    for objection_type, objection_data in OBJECTIONS.items():
        for trigger in objection_data['triggers']:
            if trigger in message_lower:
                return {
                    'detected': True,
                    'type': objection_type,
                    'response': objection_data['response']
                }
    
    return {'detected': False, 'type': None, 'response': None}


